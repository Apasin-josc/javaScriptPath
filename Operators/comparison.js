//comparison operators
let x = 1;

console.log(x > 0);
//this is going to output a TRUE, it throws a boolean True || False

console.log(x >= 1);
//throwing another true :)

console.log(x < 1);
//False

console.log(x <= 1);
//True

//Equality operators
console.log(x === 1);
//is going to throw a True
console.log(x !== 1);
//is going to throw a False because it's equal to 1 :)
