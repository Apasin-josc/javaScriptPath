//Arithmetic Operators
let x = 10;
let y = 5;

//basic operations
console.log(x + y); //addition
console.log(x - y); //substraction
console.log(x * y); //multiplication
console.log(x / y); //division
console.log(x % y); //remainder
console.log(x ** y); //exponentiation

//increment (++)
console.log(++x);
console.log(x++);
//decrement(--)
console.log(++y);
console.log(y++);
